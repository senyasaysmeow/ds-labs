# ----------------------------- Decision Support System (DSS) -------------------------------------

"""
Завдання:
   Оцінювання ефективності вибору обчислювального комплексу
   з множини альтернатив за багатокритеріальною оптимізаційною моделлю
   для ERP системи підтримки прийняття рішень.

Тип товару: Обчислювальні комплекси (сервери / робочі станції)

Склад етапів:
1. Формалізація задачі як багатофакторної / багатокритеріальної.
2. Формування сегменту даних та парсинг *.xlsx файлу.
3. Нормалізація даних.
4. Розрахунок інтегрованої оцінки (скор) — нелінійна схема компромісів Вороніна.
   Джерело: http://sci-gems.math.bas.bg/jspui/bitstream/10525/49/1/ijita15-2-p02.pdf
5. Обрання найкращої альтернативи.

Характеристика задачі:
   Кількість альтернатив (товарів): 22
   Кількість критеріїв:             14
     - Мінімізованих:                9  (C1–C9)
     - Максимізованих:               5  (C10–C14)

Критерії:
   Мінімізовані:
     C1  - Вартість придбання (тис. грн)
     C2  - Споживання електроенергії (Вт)
     C3  - Час розгортання системи (год)
     C4  - Вартість обслуговування (тис. грн/рік)
     C5  - Маса обладнання (кг)
     C6  - Рівень шуму (дБ)
     C7  - Час простою на рік (год)
     C8  - Вартість розширеної підтримки (тис. грн)
     C9  - Кількість ліцензій ПЗ (шт.)
   Максимізовані:
     C10 - Продуктивність процесора (GFLOPS)
     C11 - Об'єм оперативної пам'яті (ГБ)
     C12 - Ємність сховища (ТБ)
     C13 - Швидкість мережевого інтерфейсу (Гбіт/с)
     C14 - Рейтинг надійності постачальника (бали)

Вхідні дані: файл computing_complexes.xlsx
"""

import pandas as pd
import numpy as np


# =============================================================================
# БЛОК 1. Зчитування та парсинг файлу
# =============================================================================


def load_data(file_name: str) -> tuple[np.ndarray, list[str], list[str], list[str]]:
    """
    Зчитує Excel-файл і повертає:
      - matrix       : numpy масив форми (n_criteria, n_products)
      - criteria_names: список назв критеріїв
      - product_names : список назв товарів
      - criteria_type : список типів ('мін' / 'мах')
    """
    df = pd.read_excel(file_name)

    # Перший стовпець — назви критеріїв, останній — тип (мін/мах)
    criteria_names = df.iloc[:, 0].tolist()
    criteria_type = df.iloc[:, -1].tolist()
    product_names = list(df.columns[1:-1])

    # Матриця числових значень: рядки = критерії, стовпці = товари
    matrix = df.iloc[:, 1:-1].to_numpy(dtype=float)

    print("=" * 70)
    print("ВХІДНІ ДАНІ")
    print("=" * 70)
    print(f"  Файл                : {file_name}")
    print(f"  Кількість критеріїв : {len(criteria_names)}")
    print(f"  Кількість товарів   : {len(product_names)}")
    print(f"  Мінімізованих       : {criteria_type.count('мін')}")
    print(f"  Максимізованих      : {criteria_type.count('мах')}")
    print("=" * 70)

    return matrix, criteria_names, product_names, criteria_type


# =============================================================================
# БЛОК 2. Нормалізація (підготовка до нелінійної схеми компромісів)
# =============================================================================


def normalize_matrix(matrix: np.ndarray, criteria_type: list[str]) -> np.ndarray:
    """
    Нормалізація за формулою:
      - мінімізований критерій: F_norm[i] = F[i] / sum(F)
      - максимізований критерій: F_norm[i] = (1/F[i]) / sum(1/F)
    Повертає нормалізовану матрицю тієї ж форми (n_criteria, n_products).
    """
    n_criteria, n_products = matrix.shape
    norm_matrix = np.zeros_like(matrix, dtype=float)

    for k in range(n_criteria):
        row = matrix[k, :]
        if criteria_type[k] == "мін":
            s = np.sum(row)
            norm_matrix[k, :] = row / s
        else:  # 'мах'
            inv_row = 1.0 / row
            s = np.sum(inv_row)
            norm_matrix[k, :] = inv_row / s

    return norm_matrix


# =============================================================================
# БЛОК 3. Нелінійна схема компромісів Вороніна — інтегрована оцінка (скор)
# =============================================================================


def voronin_score(norm_matrix: np.ndarray, weights: np.ndarray) -> np.ndarray:
    """
    Нелінійна схема компромісів Вороніна:
        Integro[j] = sum_k( G_k * (1 - F_norm[k, j])^(-1) )

    Параметри:
      norm_matrix : нормалізована матриця (n_criteria, n_products)
      weights     : нормалізований вектор ваг (n_criteria,)

    Повертає вектор інтегрованих оцінок (n_products,).
    Найкращий товар — той, у якого Integro мінімальний.
    """
    n_criteria, n_products = norm_matrix.shape
    scores = np.zeros(n_products, dtype=float)

    for j in range(n_products):
        for k in range(n_criteria):
            f = norm_matrix[k, j]
            # Захист від f >= 1 (виникає у крайніх значень)
            f = min(f, 0.9999)
            scores[j] += weights[k] * (1.0 - f) ** (-1)

    return scores


# =============================================================================
# БЛОК 4. Головна функція
# =============================================================================


def evaluate(file_name: str, raw_weights: list[float]) -> None:
    """
    Повний цикл оцінювання:
      1. Завантаження даних
      2. Нормалізація матриці
      3. Нормалізація ваг критеріїв
      4. Розрахунок інтегрованих оцінок
      5. Виведення результатів та визначення оптимального комплексу
    """

    # --- 1. Завантаження ---
    matrix, criteria_names, product_names, criteria_type = load_data(file_name)

    # --- 2. Нормалізація матриці ---
    norm_matrix = normalize_matrix(matrix, criteria_type)

    # --- 3. Нормалізація ваг ---
    w = np.array(raw_weights, dtype=float)
    weights = w / w.sum()

    # --- 4. Інтегровані оцінки ---
    scores = voronin_score(norm_matrix, weights)

    # --- 5. Результати ---
    print("\nНОРМАЛІЗОВАНА МАТРИЦЯ КРИТЕРІЇВ:")
    print("-" * 70)
    header = f"{'Критерій':<45} {'Тип':<5} {'Вага':>6}"
    print(header)
    print("-" * 70)
    for k, (name, ctype, wk) in enumerate(zip(criteria_names, criteria_type, weights)):
        print(f"  {name:<43} {ctype:<5} {wk:>6.4f}")

    print("\nІНТЕГРОВАНА ОЦІНКА (СКОР) ЗА МЕТОДОМ ВОРОНІНА:")
    print("-" * 70)
    print(f"  {'Товар':<20} {'Скор':>12}  {'Ранг':>6}")
    print("-" * 70)

    ranked_indices = np.argsort(scores)
    ranks = np.empty_like(ranked_indices)
    ranks[ranked_indices] = np.arange(1, len(scores) + 1)

    for j, (name, score, rank) in enumerate(zip(product_names, scores, ranks)):
        marker = " <<< ОПТИМАЛЬНИЙ" if rank == 1 else ""
        print(f"  {name:<20} {score:>12.4f}  {rank:>6}{marker}")

    best_idx = int(np.argmin(scores))
    print("\n" + "=" * 70)
    print("РЕЗУЛЬТАТ АНАЛІЗУ:")
    print("=" * 70)
    print(f"  Оптимальний обчислювальний комплекс : {product_names[best_idx]}")
    print(f"  Інтегрована оцінка (мін = краще)    : {scores[best_idx]:.4f}")
    print("=" * 70)

    # Топ-5 найкращих
    print("\nТОП-5 НАЙКРАЩИХ АЛЬТЕРНАТИВ:")
    print("-" * 50)
    for rank_pos, idx in enumerate(ranked_indices[:5], start=1):
        print(f"  {rank_pos}. {product_names[idx]:<20}  скор = {scores[idx]:.4f}")
    print("-" * 50)


